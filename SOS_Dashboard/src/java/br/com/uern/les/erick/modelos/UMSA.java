package br.com.uern.les.erick.modelos;

/**
 *
 * @author jerick.gs
 */
public class UMSA {
 
    private String placa;
    private String cnh;
    private String cpfm;
    private String cpfe;
    private String cpfa;
    private String nomeUsuario;
    private int idG;
    private String nome;

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getCnh() {
        return cnh;
    }

    public void setCnh(String cnh) {
        this.cnh = cnh;
    }

    public String getCpfm() {
        return cpfm;
    }

    public void setCpfm(String cpfm) {
        this.cpfm = cpfm;
    }

    public String getCpfe() {
        return cpfe;
    }

    public void setCpfe(String cpfe) {
        this.cpfe = cpfe;
    }

    public String getCpfa() {
        return cpfa;
    }

    public void setCpfa(String cpfa) {
        this.cpfa = cpfa;
    }

    public String getNomeUsuario() {
        return nomeUsuario;
    }

    public void setNomeUsuario(String nomeUsuario) {
        this.nomeUsuario = nomeUsuario;
    }

    public int getIdG() {
        return idG;
    }

    public void setIdG(int idG) {
        this.idG = idG;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
}
