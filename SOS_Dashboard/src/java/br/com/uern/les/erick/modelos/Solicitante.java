package br.com.uern.les.erick.modelos;

/**
 *
 * @author jerick.gs
 */
public class Solicitante {
    
    private int idS;
    private String nome;
    private String tel;

    public int getIdS() {
        return idS;
    }

    public void setIdS(int idS) {
        this.idS = idS;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }
    
}
